import pandas as pd

# 학생 데이터 정의
data = {
    "학생명": ["김철수", "이영희", "박지민", "최우주", "정다은", "강현우", "윤서아", "임도윤"],
    "출석률": [95, 88, 92, 75, 98, 85, 90, 82],
    "학습량(시간)": [120, 105, 115, 60, 130, 95, 110, 88]
}

df = pd.DataFrame(data)

# 성취도 점수 산출 로직 (가중치 적용: 출석률 40% + 학습량 60%)
# 출석률과 학습량은 단위가 다르므로(백분율 vs 시간), 정규화(0~100 스케일)를 거쳐 계산하는 것이 일반적입니다.
df["출석_표준"] = (df["출석률"] / df["출석률"].max()) * 100
df["학습_표준"] = (df["학습량(시간)"] / df["학습량(시간)"].max()) * 100

# 최종 점수 계산
df["성취도점수"] = (df["출석_표준"] * 0.4 + df["학습_표준"] * 0.6).round(1)

# 불필요한 표준화 컬럼 제거 및 CSV 저장
df = df.drop(columns=["출석_표준", "학습_표준"])
df.to_csv("student_data.csv", index=False, encoding='utf-8-sig')

print("student_data.csv 파일이 성공적으로 생성되었습니다.")
print(df)